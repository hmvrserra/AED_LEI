public class Main {
    public static void main(String[] args) {
        Integer[] a = {4, 2, 6, 7, 9, 8, 10, 3, 5, 1};
        System.out.println("Before sorting:");
        for (int i = 0; i < a.length; i++)
            System.out.print(a[i] + " ");
        System.out.println("\nAfter sorting:");
        for (int i = 0; i < a.length; i++)
            System.out.print(a[i] + " ");
        System.out.println();
    }
}